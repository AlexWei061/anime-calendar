import { mkdir, rm, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import sharp from "sharp";
import { allAnime } from "../data/anime.js";

const projectRoot = dirname(dirname(fileURLToPath(import.meta.url)));
const coverDirectory = join(projectRoot, "public", "covers", "yuc");
const spriteDirectory = join(coverDirectory, "sprites");
const cellWidth = 600;
const cellHeight = 750;
const columns = 4;
const rows = 10;
const coversPerSprite = columns * rows;

const covers = [...allAnime].sort(({ coverUrl: left }, { coverUrl: right }) => left.localeCompare(right));
if (new Set(covers.map(({ coverUrl }) => coverUrl)).size !== covers.length) {
  throw new Error("Each anime needs a unique cover URL before generating sprites.");
}

await mkdir(spriteDirectory, { recursive: true });
const sprites = {};

for (let offset = 0; offset < covers.length; offset += coversPerSprite) {
  const page = covers.slice(offset, offset + coversPerSprite);
  const spriteRows = Math.ceil(page.length / columns);
  const spriteUrl = `/covers/yuc/sprites/cover-sheet-${String(offset / coversPerSprite + 1).padStart(2, "0")}.webp`;
  const composite = await Promise.all(
    page.map(async ({ coverUrl }, index) => ({
      input: await sharp(join(projectRoot, "public", coverUrl.slice(1)))
        .resize(cellWidth, cellHeight, { fit: "cover" })
        .toBuffer(),
      left: (index % columns) * cellWidth,
      top: Math.floor(index / columns) * cellHeight,
    })),
  );

  await sharp({
    create: {
      width: columns * cellWidth,
      height: spriteRows * cellHeight,
      channels: 4,
      background: { r: 255, g: 255, b: 255, alpha: 0 },
    },
  })
    .composite(composite)
    .webp({ quality: 75, effort: 4 })
    .toFile(join(projectRoot, "public", spriteUrl.slice(1)));

  for (const [index, { coverUrl }] of page.entries()) {
    sprites[coverUrl] = {
      url: spriteUrl,
      x: index % columns,
      y: Math.floor(index / columns),
      columns,
      rows: spriteRows,
    };
  }
}

await writeFile(
  join(projectRoot, "data", "cover-sprites.js"),
  "// Generated by scripts/generate-cover-sprites.mjs.\n" +
    "export const coverSprites = Object.freeze(" +
    JSON.stringify(sprites, null, 2) +
    ");\n\n" +
    "export function coverSpriteFor(coverUrl) {\n  return coverSprites[coverUrl] ?? null;\n}\n",
);

await Promise.all(covers.map(({ coverUrl }) => rm(join(projectRoot, "public", coverUrl.slice(1)))));
console.log(`Packed ${covers.length} covers into ${Math.ceil(covers.length / coversPerSprite)} WebP sprites.`);

import { writeFile } from "node:fs/promises";
import { pathToFileURL } from "node:url";

import {
  choosePrimaryTelevisionSchedule,
  compressEpisodeSchedules,
  episodeRange,
  parseChannelLookup,
  parseProgLookup,
  parseTitleLookup,
  syoboiJstToBeijing,
} from "../lib/syoboi.js";
import { yucSeasonsForYear } from "./generate-yuc-history-pilot.mjs";

const SOURCE_URL = "https://cal.syoboi.jp/";
const USER_AGENT = "anime-calendar-schedule-audit/1.0 (+https://github.com/AlexWei061/anime-calendar)";
const REQUEST_DELAY_MS = 1_100;

export const SYOBOI_TITLE_ALIASES = Object.freeze({});

export function normalizeSyoboiTitle(value) {
  return value
    .normalize("NFKC")
    .toLowerCase()
    .replace(/[\s\p{P}\p{S}]+/gu, "");
}

export function resolveSyoboiTitle(record, titles, year) {
  const alias = SYOBOI_TITLE_ALIASES[record.id];
  if (alias) return { status: "matched", tid: alias.tid };

  const names = new Set(
    [record.titleJa, record.aniListTitleJa].filter(Boolean).map((title) => normalizeSyoboiTitle(title)),
  );
  const candidates = titles.filter(
    ({ firstYear, tid, title }) =>
      firstYear >= year - 1 && firstYear <= year && names.has(normalizeSyoboiTitle(title)) && Number.isInteger(tid),
  );

  if (candidates.length === 1) return { status: "matched", tid: candidates[0].tid };
  if (candidates.length === 0) return { status: "unmatched" };
  return { status: "ambiguous", candidateTids: candidates.map(({ tid }) => tid).sort((left, right) => left - right) };
}

function actualEpisodeSchedules(programs, channelId) {
  const uniqueEpisodes = new Map();
  for (const program of programs) {
    if (program.channelId !== channelId || program.deleted !== 0 || (program.flag & 0x8) !== 0) continue;
    const range = episodeRange(program);
    if (!range) continue;
    try {
      const schedule = { ...range, ...syoboiJstToBeijing(program.stTime) };
      const key = `${schedule.episodeStart}-${schedule.episodeEnd}`;
      if (!uniqueEpisodes.has(key)) uniqueEpisodes.set(key, schedule);
    } catch {}
  }
  return compressEpisodeSchedules([...uniqueEpisodes.values()]);
}

export function buildYearSnapshot({ year, catalog, titles, channels, programsByTid, generatedAt = "1970-01-01" }) {
  const entries = [];
  const unmatched = [];
  const ambiguous = [];
  const skipped = [];

  for (const record of catalog) {
    const resolution = resolveSyoboiTitle(record, titles, year);
    if (resolution.status === "unmatched") {
      unmatched.push({ recordId: record.id });
      continue;
    }
    if (resolution.status === "ambiguous") {
      ambiguous.push({ recordId: record.id, candidateTids: resolution.candidateTids });
      continue;
    }

    const programs = programsByTid.get(resolution.tid) ?? [];
    const primary = choosePrimaryTelevisionSchedule(programs, channels);
    if (!primary) {
      skipped.push({ recordId: record.id, tid: resolution.tid, reason: "no-eligible-television-premiere" });
      continue;
    }

    const episodeSchedules = actualEpisodeSchedules(programs, primary.channelId);
    if (!episodeSchedules.length) {
      skipped.push({ recordId: record.id, tid: resolution.tid, reason: "no-numbered-episodes" });
      continue;
    }

    entries.push({
      recordId: record.id,
      tid: resolution.tid,
      titleJa: record.titleJa,
      channel: channels.get(primary.channelId).name,
      sourceUrl: `https://cal.syoboi.jp/tid/${resolution.tid}`,
      episodeSchedules,
    });
  }

  return {
    generatedAt,
    sourceName: "しょぼいカレンダー",
    sourceUrl: SOURCE_URL,
    entries: entries.sort((left, right) => left.recordId.localeCompare(right.recordId) || left.tid - right.tid),
    unmatched: unmatched.sort((left, right) => left.recordId.localeCompare(right.recordId)),
    ambiguous: ambiguous.sort((left, right) => left.recordId.localeCompare(right.recordId)),
    skipped: skipped.sort((left, right) => left.recordId.localeCompare(right.recordId)),
  };
}

export async function writeYearSnapshot(year, snapshot) {
  const exportName = `syoboiHistory${year}`;
  const output = `// Generated by scripts/generate-syoboi-history.mjs. Do not edit by hand.\n\nexport const ${exportName} = ${JSON.stringify(snapshot, null, 2)};\n`;
  await writeFile(new URL(`../data/syoboi-history-${year}.js`, import.meta.url), output);
}

function wait(milliseconds = REQUEST_DELAY_MS) {
  return new Promise((resolve) => setTimeout(resolve, milliseconds));
}

export async function requestWithRateLimitRetry(request, sleep = wait) {
  const response = await request();
  if (response.status !== 429) return response;
  await sleep(10_000);
  return request();
}

async function fetchXml(command, params = {}) {
  const url = new URL("db.php", SOURCE_URL);
  url.searchParams.set("Command", command);
  for (const [key, value] of Object.entries(params)) url.searchParams.set(key, value);
  const response = await requestWithRateLimitRetry(() => fetch(url, { headers: { "user-agent": USER_AGENT } }));
  if (!response.ok) throw new Error(`Syoboi request failed (${response.status}): ${url}`);
  await wait();
  return response.text();
}

function formatRangeDate(date) {
  return `${date.toISOString().slice(0, 10).replaceAll("-", "")}_000000`;
}

function rangeForYear(year) {
  const start = new Date(Date.UTC(year, 0, 1));
  const end =
    year === 2026
      ? new Date(Date.now() + 24 * 60 * 60 * 1000)
      : new Date(Date.UTC(year + 1, 0, 1));
  return `${formatRangeDate(start)}-${formatRangeDate(end)}`;
}

function quarterRanges(year) {
  return [0, 3, 6, 9].map((month) => {
    const start = new Date(Date.UTC(year, month, 1));
    const end = month === 9 ? new Date(Date.UTC(year + 1, 0, 1)) : new Date(Date.UTC(year, month + 3, 1));
    return `${formatRangeDate(start)}-${formatRangeDate(end)}`;
  });
}

async function fetchPrograms(tid, year) {
  const programs = parseProgLookup(await fetchXml("ProgLookup", { TID: tid, Range: rangeForYear(year) }));
  if (programs.length < 5000) return programs;
  return (await Promise.all(quarterRanges(year).map(async (range) => parseProgLookup(await fetchXml("ProgLookup", { TID: tid, Range: range })))))
    .flat()
    .sort((left, right) => left.id - right.id);
}

async function catalogForYear(year) {
  const configs = yucSeasonsForYear(year);
  const [catalogs, aniListCatalogs] = await Promise.all([
    import(new URL(`../data/yuc-history-${year}.js`, import.meta.url)),
    import(new URL(`../data/anilist-${year}.js`, import.meta.url)),
  ]);
  const aniListById = new Map(
    configs
      .flatMap(({ aniListExportName }) => aniListCatalogs[aniListExportName].anime)
      .map((record) => [Number(record.id.replace("anilist-", "")), record]),
  );
  return configs.flatMap(({ exportName }) =>
    catalogs[exportName].anime.map((record) => ({
      ...record,
      aniListTitleJa: record.anilistId === null ? null : aniListById.get(record.anilistId)?.titleJa ?? null,
    })),
  );
}

async function main() {
  const year = Number(process.argv[2] ?? "2026");
  if (!Number.isInteger(year) || year < 2020 || year > 2026) throw new RangeError("Year must be between 2020 and 2026");

  const [titleXml, channelXml, catalog] = [
    await fetchXml("TitleLookup", { TID: "*" }),
    await fetchXml("ChLookup"),
    await catalogForYear(year),
  ];
  const titles = parseTitleLookup(titleXml);
  const channels = new Map(parseChannelLookup(channelXml).map((channel) => [channel.id, channel]));
  const matchedTids = new Set(
    catalog
      .map((record) => resolveSyoboiTitle(record, titles, year))
      .filter(({ status }) => status === "matched")
      .map(({ tid }) => tid),
  );
  const programsByTid = new Map();
  for (const tid of [...matchedTids].sort((left, right) => left - right)) programsByTid.set(tid, await fetchPrograms(tid, year));

  const snapshot = buildYearSnapshot({
    year,
    catalog,
    titles,
    channels,
    programsByTid,
    generatedAt: new Intl.DateTimeFormat("en-CA", { timeZone: "Asia/Shanghai" }).format(new Date()).replaceAll("/", "-"),
  });
  await writeYearSnapshot(year, snapshot);
  console.log(`Syoboi ${year}: matched ${snapshot.entries.length}, unmatched ${snapshot.unmatched.length}, ambiguous ${snapshot.ambiguous.length}, skipped ${snapshot.skipped.length}`);
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) await main();
